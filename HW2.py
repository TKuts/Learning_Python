# 1. Написати програму, яка проходиться по числам від 1 до 100 та виводить лише ті, які діляться на 4-и!
# 2. Створіть список з чисел (числа додайте самостійно наприклад [1, 23, 3, 5, 9, 2, 17]) пройдіться по цьому списку чисел та виведіть лише ті які більші за 10. 

# 1
for number in range(1, 101):
	if number % 4 == 0:
		print(number)

# 2
number_list = [1, 23, 3, 5, 9, 2, 17]

for number in number_list:
	if number > 10:
		print(number)
